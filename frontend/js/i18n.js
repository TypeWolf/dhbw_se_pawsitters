const I18n = {
    get lang() { return localStorage.getItem('paws.lang') || 'en'; },
    set lang(value) { localStorage.setItem('paws.lang', value); },
    t(key) {
        return (translations[this.lang] && translations[this.lang][key]) || translations.en[key] || key;
    },
    apply(root = document) {
        root.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const value = this.t(key);
            if (el.tagName === 'INPUT' && el.type !== 'submit' && el.type !== 'button') {
                el.placeholder = value;
            } else {
                el.textContent = value;
            }
        });
    },
    setLanguage(lang) {
        this.lang = lang;
        this.apply();
        document.dispatchEvent(new CustomEvent('paws:lang', { detail: lang }));
    }
};
